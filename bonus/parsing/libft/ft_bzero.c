#include <stdio.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char		*p;
	size_t		i;

	i = 0;
	p = (char *) s;
	if (n == 0)
		return ;
	while (i < n)
	{
		p[i] = '\0';
		i++;
	}
}
